public class PhotovoltaicIncomeVo {
  private String periodName;
  private Double epesum;
  private Double episum;
  private Double epipricesum;
  private Double epepricesum;

  public String getPeriodName() {
    return periodName;
  }

  public void setPeriodName(String periodName) {
    this.periodName = periodName;
  }

  public Double getEpesum() {
    return epesum;
  }

  public void setEpesum(Double epesum) {
    this.epesum = epesum;
  }

  public Double getEpisum() {
    return episum;
  }

  public void setEpisum(Double episum) {
    this.episum = episum;
  }

  public Double getEpipricesum() {
    return epipricesum;
  }

  public void setEpipricesum(Double epipricesum) {
    this.epipricesum = epipricesum;
  }

  public Double getEpepricesum() {
    return epepricesum;
  }

  public void setEpepricesum(Double epepricesum) {
    this.epepricesum = epepricesum;
  }
}
