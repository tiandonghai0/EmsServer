package com.shmet.bean;

public class GroupStatisticsItem {
    String groupNo;
    String groupTagCode;
    Object groupData;

    public String getGroupNo() {
        return groupNo;
    }

    public void setGroupNo(String groupNo) {
        this.groupNo = groupNo;
    }

    public String getGroupTagCode() {
        return groupTagCode;
    }

    public void setGroupTagCode(String groupTagCode) {
        this.groupTagCode = groupTagCode;
    }

    public Object getGroupData() {
        return groupData;
    }

    public void setGroupData(Object groupData) {
        this.groupData = groupData;
    }

}
